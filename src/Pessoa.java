
public class Pessoa {
	
	private String nome ="";
	private String sobrenome="";
	private int sala1;
	private int sala2;
	private int cafe1;
	private int cafe2;
	
	public String getNome() {
		return nome;
	}
	
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	public String getSobrenome(){
		return sobrenome;
	}
	
	public void setSobrenome(String sobrenome) {
		this.sobrenome = sobrenome;
	}

	public int getSala1() {
		return sala1;
	}

	public void setSala1(int sala) {
		this.sala1 = sala;
	}

	public int getSala2() {
		return sala2;
	}

	public void setSala2(int sala2) {
		this.sala2 = sala2;
	}

	public int getCafe1() {
		return cafe1;
	}

	public void setCafe1(int cafe) {
		this.cafe1 = cafe;
	}

	public int getCafe2() {
		return cafe2;
	}

	public void setCafe2(int cafe2) {
		this.cafe2 = cafe2;
	}

}
